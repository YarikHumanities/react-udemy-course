import { useState } from "react";

export default function Player({
  initialName,
  symbol,
  isActive,
  onChangeName,
}) {
  const [isEditing, setIsEditing] = useState(false);
  const [playerName, setPlayerName] = useState(initialName);

  function editButtonClickHandling() {
    //When you call setIsEditing and pass a function to it,
    //React internally passes the CURRENT state value as
    //an argument to that function. !!!CURRENT BY THE MOMENT OF SCHEDULED EXECUTION OF STATE CHANGING
    setIsEditing((editing) => !editing);
    if (isEditing) {
      onChangeName(symbol, playerName);
    }
  }

  function handleChange(event) {
    console.log(event);
    setPlayerName(event.target.value);
  }

  return (
    <li className={isActive ? "active" : undefined}>
      <span className="player">
        {!isEditing ? (
          <span className="player-name">{playerName}</span>
        ) : (
          <input
            type="text"
            required
            value={playerName}
            onChange={handleChange}
          ></input>
        )}
        <span className="player-symbol">{symbol}</span>
      </span>
      <button onClick={editButtonClickHandling}>
        {isEditing ? "Save" : "Edit"}
      </button>
    </li>
  );
}
